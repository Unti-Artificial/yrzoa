package com.yrz.oa.core.po;

public class ClockToday {
    private Integer todayDegree;
    private String clockCreateTime;
    private String userName;

    public Integer getTodayDegree() {
        return todayDegree;
    }

    public void setTodayDegree(Integer todayDegree) {
        this.todayDegree = todayDegree;
    }

    public String getClockCreateTime() {
        return clockCreateTime;
    }

    public void setClockCreateTime(String clockCreateTime) {
        this.clockCreateTime = clockCreateTime;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }
}
