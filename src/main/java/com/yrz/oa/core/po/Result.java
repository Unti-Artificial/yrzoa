package com.yrz.oa.core.po;

public class Result {
     private String approveId;
     private String  approveFinalResult;
     private String approveFinalUser;
     private String approveFinalCreateTime;
     private String applyUser;


    public String getApproveId() {
        return approveId;
    }

    public void setApproveId(String approveId) {
        this.approveId = approveId;
    }

    public String getApproveFinalResult() {
        return approveFinalResult;
    }

    public void setApproveFinalResult(String approveFinalResult) {
        this.approveFinalResult = approveFinalResult;
    }

    public String getApproveFinalUser() {
        return approveFinalUser;
    }

    public void setApproveFinalUser(String approveFinalUser) {
        this.approveFinalUser = approveFinalUser;
    }

    public String getApproveFinalCreateTime() {
        return approveFinalCreateTime;
    }

    public void setApproveFinalCreateTime(String approveFinalCreateTime) {
        this.approveFinalCreateTime = approveFinalCreateTime;
    }

    public String getApplyUser() {
        return applyUser;
    }

    public void setApplyUser(String applyUser) {
        this.applyUser = applyUser;
    }
}
